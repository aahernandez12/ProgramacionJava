package doWhile;

public class ejercicio1 {

	public static void main(String[] args) {
		// Ejemplo de While y Do While
		 int i = 0;
		    
		    while(i != 0)
		    {
		      System.out.println("WHILE");  // No se ejecuta.
		    }
		    
		    do {
		      System.out.println("DO-WHILE"); // Se ejecuta una vez.
		    } while(i != 0);

	}

}
